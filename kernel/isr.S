.extern isr_handler

.macro ISR_NOERR n
isr\n:
    push 0          // fake error code
    push \n         // interrupt number
    jmp isr_common
.endm

.macro ISR_ERR n
isr\n:
    push \n         // interrupt number (CPU already pushed error code)
    jmp isr_common
.endm

// Common stub: stack on entry
//  NOERR: [int_no][err=0][eip][cs][eflags]...
//   ERR : [int_no][err ][eip][cs][eflags]...
isr_common:
    mov eax, [esp]      // int_no
    mov ebx, [esp+4]    // err_code

    pushad

    // Ensure data segments are sane
    mov ax, 0x10
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax

    push ebx
    push eax
    call isr_handler
    add esp, 8

    popad
    add esp, 8          // pop int_no + err_code
    iret

